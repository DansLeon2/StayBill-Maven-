    private void limpiarFiltros() {
    // Reset filtro principal
    if (cb_campo.getItemCount() > 0) {
        cb_campo.setSelectedIndex(0); // "Todos"
    }

    // Reset texto de búsqueda
    txt_busqueda.setText("");
    txt_busqueda.setEnabled(true);
    lbl_valor.setText("Filtro: Todos");

    // Reset fechas
    dp_inicio.setDate(null);
    dp_fin.setDate(null);

    // Ejecuta búsqueda limpia
    buscarFacturas();

}


    private void exportarExcel() {
        javax.swing.JFileChooser chooser = new javax.swing.JFileChooser();
        chooser.setDialogTitle("Guardar reporte como Excel (CSV)");
        chooser.setSelectedFile(new java.io.File("reporte_ventas.csv"));

        int result = chooser.showSaveDialog(this);
        if (result != javax.swing.JFileChooser.APPROVE_OPTION) {
            return;
        }

        java.io.File file = chooser.getSelectedFile();

        try (java.io.BufferedWriter bw = new java.io.BufferedWriter(new java.io.FileWriter(file))) {

            for (int col = 0; col < tabla_reportes.getColumnCount(); col++) {
                bw.write(tabla_reportes.getColumnName(col));
                if (col < tabla_reportes.getColumnCount() - 1) {
                    bw.write(";");
                }
            }
            bw.newLine();

            for (int row = 0; row < tabla_reportes.getRowCount(); row++) {
                for (int col = 0; col < tabla_reportes.getColumnCount(); col++) {
                    Object valor = tabla_reportes.getValueAt(row, col);
                    bw.write(valor == null ? "" : valor.toString());
                    if (col < tabla_reportes.getColumnCount() - 1) {
                        bw.write(";");
                    }
                }
                bw.newLine();
            }

            bw.flush();

            JOptionPane.showMessageDialog(
                    this,
                    "Archivo guardado correctamente.\nPuedes abrirlo con Excel.",
                    "Exportar a Excel",
                    JOptionPane.INFORMATION_MESSAGE
            );

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage(),
                    "Error al exportar a Excel",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }

