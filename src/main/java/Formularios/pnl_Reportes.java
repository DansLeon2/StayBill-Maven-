    private void buscarFacturas() {
    try {
        String campo = (cb_campo.getSelectedItem() == null) ? "Todos" : cb_campo.getSelectedItem().toString();
        String texto = (txt_busqueda.getText() == null) ? "" : txt_busqueda.getText().trim();
        java.util.Date dIni = dp_inicio.getDate();
        java.util.Date dFin = dp_fin.getDate();

        String where = buildWhere(campo, texto, dIni, dFin);

        String sql =
            "SELECT " +
            " cf.num_fac  AS 'N° Factura', " +
            " cf.fec_fac  AS 'Fecha', " +
            " cf.ced_cli  AS 'Cédula', " +
            " cf.sub_fac  AS 'Subtotal', " +
            " cf.desc_fac AS 'Descuento', " +
            " cf.iva_fac  AS 'IVA', " +
            " cf.tot_fac  AS 'Total', " +
            " u.nom_usu   AS 'Usuario', " +
            " h.cod_hab   AS 'Habitación' " +
            "FROM cabecera_factura cf " +
            "LEFT JOIN usuarios u ON cf.id_usuario = u.id_usu " +
            "LEFT JOIN habitaciones h ON cf.id_hab = h.id_hab " +
            where +
            " ORDER BY cf.fec_fac DESC, cf.num_fac DESC;";

        Clases.cls_conexion cnx = new Clases.cls_conexion();
        cnx.CargarTabla(sql, tabla_reportes);

        // cards
        actualizarCards(where);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage(), "Error al buscar facturas", JOptionPane.ERROR_MESSAGE);
    }
}
    
    private String buildWhere(String campo, String texto, java.util.Date dIni, java.util.Date dFin) {
    java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
    String t = (texto == null) ? "" : texto.trim();
    t = t.replace("'", "''"); // evita romper SQL

    StringBuilder where = new StringBuilder(" WHERE 1=1 ");

    // Fechas
    if (dIni != null) where.append(" AND DATE(cf.fec_fac) >= '").append(sdf.format(dIni)).append("' ");
    if (dFin != null) where.append(" AND DATE(cf.fec_fac) <= '").append(sdf.format(dFin)).append("' ");

    if (t.isEmpty()) return where.toString();

    // Normaliza "H8" a "8"
    String habNumero = null;
    if (t.matches("(?i)h\\d+")) {
        habNumero = t.substring(1); // quita H
    }

    // Filtros
    switch (campo) {
        case "Usuario" -> where.append(" AND u.nom_usu LIKE '%").append(t).append("%' ");
        case "Cliente (Cédula)" -> where.append(" AND cf.ced_cli LIKE '%").append(t).append("%' ");
        case "N° Factura" -> where.append(" AND CAST(cf.num_fac AS CHAR) LIKE '%").append(t).append("%' ");
        case "Habitación" -> {
            String v = (habNumero != null) ? habNumero : t;
            where.append(" AND (CAST(cf.id_hab AS CHAR) LIKE '%").append(v).append("%' OR h.cod_hab LIKE '%").append(t).append("%') ");
        }
        default -> {
            // TODOS: usuario / cédula / num factura / habitación (id o H#)
            where.append(" AND (")
                 .append(" u.nom_usu LIKE '%").append(t).append("%' ")
                 .append(" OR cf.ced_cli LIKE '%").append(t).append("%' ")
                 .append(" OR CAST(cf.num_fac AS CHAR) LIKE '%").append(t).append("%' ")
                 .append(" OR CAST(cf.id_hab AS CHAR) LIKE '%").append((habNumero != null ? habNumero : t)).append("%' ")
                 .append(" OR h.cod_hab LIKE '%").append(t).append("%' ")
                 .append(" ) ");
        }
    }

    return where.toString();
}
    
    private void actualizarCards(String where) {
    try {
        Clases.cls_conexion cnx = new Clases.cls_conexion();

        // 1) Total ventas + total facturas
        String sqlTotales =
            "SELECT COUNT(*) AS n, IFNULL(SUM(cf.tot_fac),0) AS total " +
            "FROM cabecera_factura cf " +
            "LEFT JOIN usuarios u ON cf.id_usuario = u.id_usu " +
            "LEFT JOIN habitaciones h ON cf.id_hab = h.id_hab " +
            where + ";";

        java.sql.ResultSet rs = cnx.Consulta(sqlTotales);
        int n = 0;
        double total = 0;
        if (rs != null && rs.next()) {
            n = rs.getInt("n");
            total = rs.getDouble("total");
        }
        if (rs != null) rs.close();

        lbl_tv_value.setText(String.format(java.util.Locale.US, "$ %.2f", total));
        lbl_tf_value.setText(String.valueOf(n));

        // 2) Habitación más usada (por cantidad)
        String sqlHabTop =
            "SELECT IFNULL(h.cod_hab, CONCAT('Hab ', cf.id_hab)) AS hab, COUNT(*) AS c " +
            "FROM cabecera_factura cf " +
            "LEFT JOIN habitaciones h ON cf.id_hab = h.id_hab " +
            "LEFT JOIN usuarios u ON cf.id_usuario = u.id_usu " +
            where +
            " GROUP BY hab ORDER BY c DESC LIMIT 1;";

        rs = cnx.Consulta(sqlHabTop);
        if (rs != null && rs.next()) {
            lbl_hab_value.setText(rs.getString("hab") + " (" + rs.getInt("c") + ")");
        } else {
            lbl_hab_value.setText("-");
        }
        if (rs != null) rs.close();

        // 3) Usuario con más ventas (por dinero)
        String sqlUsuTop =
            "SELECT IFNULL(u.nom_usu,'(Sin usuario)') AS usu, IFNULL(SUM(cf.tot_fac),0) AS t " +
            "FROM cabecera_factura cf " +
            "LEFT JOIN usuarios u ON cf.id_usuario = u.id_usu " +
            "LEFT JOIN habitaciones h ON cf.id_hab = h.id_hab " +
            where +
            " GROUP BY usu ORDER BY t DESC LIMIT 1;";

        rs = cnx.Consulta(sqlUsuTop);
        if (rs != null && rs.next()) {
            lbl_usr_value.setText(rs.getString("usu") + " ($" + String.format(java.util.Locale.US,"%.2f", rs.getDouble("t")) + ")");
        } else {
            lbl_usr_value.setText("-");
        }
        if (rs != null) rs.close();

    } catch (Exception e) {
        // No revientes el panel por fallar cards
        lbl_tv_value.setText("-");
        lbl_tf_value.setText("-");
        lbl_hab_value.setText("-");
        lbl_usr_value.setText("-");
    }
}




